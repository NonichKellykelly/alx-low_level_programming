#include "main.h"

/**
 * print_sign - function that computes the absolute value of an integer
 *
 * @n: parameter to be checked
 *
 * Return: 1,0 and -1
 */

int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
		(n = 0);
		_putchar('0');
		return (0);
	{
		_putchar('-');
		return (-1);
	}
}
